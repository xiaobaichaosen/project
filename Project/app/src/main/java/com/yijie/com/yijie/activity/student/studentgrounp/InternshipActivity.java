package com.yijie.com.yijie.activity.student.studentgrounp;

import android.content.Intent;
import android.graphics.Color;
import android.os.Bundle;
import android.support.v4.widget.SwipeRefreshLayout;
import android.support.v7.widget.LinearLayoutManager;
import android.support.v7.widget.RecyclerView;
import android.view.View;
import android.widget.ImageView;
import android.widget.RelativeLayout;
import android.widget.TextView;

import com.yijie.com.yijie.R;
import com.yijie.com.yijie.activity.school.StudentBean;
import com.yijie.com.yijie.activity.student.NotPassActivity;
import com.yijie.com.yijie.activity.student.StudentActivity;
import com.yijie.com.yijie.activity.student.adapter.LoadMoreCheckPendindAdapter;
